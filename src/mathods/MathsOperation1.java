package mathods;

public class MathsOperation1 {

	public void remainder() {
		int a=5;
		int b=2;
		int c=a%b;
		System.out.println("Remainder is = "+c);
	}
	public void remainder(int a,int b) {
		int c=a%b;
		System.out.println("Remainder is = "+c);
	}
	public static void division() {
		float a=5f;
		float b=2f;
		float c=a/b;
		System.out.println("Division is = "+c);
	}
	public static void division(float a,float b) {
		float c=a/b;
		System.out.println("Division is = "+c);
	}
}
